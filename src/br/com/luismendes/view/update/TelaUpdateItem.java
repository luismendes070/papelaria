    private void jbUpdateItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbUpdateItemActionPerformed

        ControllerItemVenda controllerItemVenda = new ControllerItemVenda();

        int novaQtdItens;

        try {
            
            novaQtdItens = Integer.parseInt(jtfQuantidadeItem.getText());
            itemEditando.setQuantidadeItens(novaQtdItens);

            controllerItemVenda = new ControllerItemVenda();
            controllerItemVenda.editar(itemEditando, jtItensVenda, venda, listaCompras, jtfPrecoTotal, jtProdutos);
            dispose();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro na quantidade de itens:\n"+e);
        }


    }//GEN-LAST:event_jbUpdateItemActionPerformed

