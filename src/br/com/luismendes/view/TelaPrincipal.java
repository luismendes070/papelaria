    private void jbEditarVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEditarVendaActionPerformed

        if (jtVendas.getSelectedRow() > -1) {

            try {

                int idVenda = (int) jtVendas.getValueAt(jtVendas.getSelectedRow(), 0);

                BDVendas bdv = new BDVendas();
                ModelVendas venda = bdv.buscarVenda(idVenda);

                if (venda != null) {

                    //QUANDO UMA VENDA É ATUALIZADA,
                    //O CPF DO USUÁRIO É ATUALIZADO PARA O CPF DO FUNCIONÁRIO QUE FIZER A ATUALIZAÇÃO
                    venda.setCpfFuncionario(CPF_FUNCIONARIO);

                    jbNovaVenda.setEnabled(false);
                    jbExcluirVenda.setEnabled(false);
                    jbBuscarCPFClienteVendas.setEnabled(false);

                    TelaUpdateVenda telaUpdateVenda
                            = new TelaUpdateVenda(venda, jtVendas, jtClientes,
                                    jtProdutos, jbNovaVenda, jbExcluirVenda,
                                    jbBuscarCPFClienteVendas);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao recuperar código de venda da tabela vendas!\n" + e);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Você deve escolher uma linha da tabela!");
        }
    }//GEN-LAST:event_jbEditarVendaActionPerformed

