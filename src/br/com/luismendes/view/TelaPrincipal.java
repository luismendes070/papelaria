    private void jbExcluirVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirVendaActionPerformed
        
        int linha = jtVendas.getSelectedRow();

        if (linha > -1) {

            int idVenda = Integer.parseInt(jtVendas.getModel().getValueAt(linha, 0).toString());

            BDVendas bdv = new BDVendas();
            ModelVendas mv = bdv.buscarVenda(idVenda);

            if (mv != null) {

                String cpf = mv.getCpfCliente();

                BDClientes bdc = new BDClientes();
                ModelClientes mc = bdc.buscar(cpf);

                if (mc != null) {
                    String nome = mc.getNome();

                    int resposta = JOptionPane.showConfirmDialog(null, ""
                            + "\n Código de Venda: " + idVenda
                            + "\n Cliente: " + nome,
                            "Excluir venda",
                            JOptionPane.YES_NO_OPTION);

                    if (resposta == JOptionPane.YES_OPTION) {

                        ControllerVenda controllerVenda = new ControllerVenda();
                        controllerVenda.cancelaVendas(mv);

                        BDVendas bdVendas = new BDVendas();
                        bdVendas.preencheTabela("select * from vendas", jtVendas);
                    }
                }
            }//fim if 
        } else {
            JOptionPane.showMessageDialog(null, "Você deve escolher uma linha da tabela!");
        }
        BDVendas bdVendas = new BDVendas();
        bdVendas.preencheTabela("select * from vendas", jtVendas);
    }//GEN-LAST:event_jbExcluirVendaActionPerformed

